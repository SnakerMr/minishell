#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		x;

	x = 0;
	while (s[x])
		x++;
	x++;
	while (x-- != 0)
		if (s[x] == (char)c)
			return ((char *)&s[x]);
	return (0);
}
