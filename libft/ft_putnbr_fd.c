#include "libft.h"

void	ft_putnbr_fd(long int n, int fd)
{
	char	c;

	if (n == -2147483647)
		write (fd, "-2147483647", 20);
	else if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
		ft_putnbr_fd(n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		c = '0' + n;
		write (fd, &c, 1);
	}
}
